package com.example.mysocial;

public class Task {


    String cause;
    int date;
    String location;
    String requirement1;
    String requirement2;

    public String getCause() {
        return cause;
    }

    public void setCause(String cause) {
        this.cause = cause;
    }

    public int getDate() {
        return date;
    }

    public void setDate(int date) {
        this.date = date;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getRequirement1() {
        return requirement1;
    }

    public void setRequirement1(String requirement1) {
        this.requirement1 = requirement1;
    }

    public String getRequirement2() {
        return requirement2;
    }

    public void setRequirement2(String requirement2) {
        this.requirement2 = requirement2;
    }
}
